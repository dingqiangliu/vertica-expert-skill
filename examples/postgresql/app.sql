CREATE OR REPLACE FUNCTION P_GET_JDH(
    OUT ac_jdh VARCHAR(100),
    OUT rt INTEGER
)
AS $$
BEGIN
    SELECT CSNR INTO ac_jdh FROM XT_XTCS WHERE CSXH = '10001' AND JG_DM = 'PUBLIC';
    IF ac_jdh IS NULL THEN
        RAISE EXCEPTION '没有初始化"节点号"[CSXH = ''10001'']系统参数！';
    END IF;

    rt := 100;
END;
$$ LANGUAGE plpgsql;


CREATE OR REPLACE FUNCTION P_GET_JD_DM(
    AC_FULL VARCHAR,
    AC_FLAG VARCHAR
)
RETURNS VARCHAR(30) AS $$
DECLARE
    LC_FULL     VARCHAR(1000);
    LN_POS      INTEGER;
    LC_JD_MC    VARCHAR(1000);
    LC_FJD_DM   VARCHAR(30);
BEGIN
    LC_FULL := AC_FULL;
    LC_FJD_DM := '0';
    LOOP
        LN_POS := POSITION('~' IN LC_FULL);
        IF LN_POS = 0 THEN
            LC_JD_MC := LC_FULL;
        ELSE
            LC_JD_MC := SUBSTR(LC_FULL, 1, LN_POS - 1);
            LC_FULL := SUBSTR(LC_FULL, LN_POS + 1);
        END IF;
        BEGIN
            IF AC_FLAG = '0' THEN
                SELECT JD_DM INTO LC_FJD_DM FROM QX_GNMK_TREE WHERE FJD_DM = LC_FJD_DM AND JD_MC = LC_JD_MC;
            ELSE
                SELECT JD_DM INTO LC_FJD_DM FROM QX_GNMB_GNMK WHERE FJD_DM = LC_FJD_DM AND JD_MC = LC_JD_MC AND GNMB_DM = '00000000001';
            END IF;
        EXCEPTION
            WHEN OTHERS THEN
                DECLARE
                    v_error_msg VARCHAR;
                BEGIN
                    GET STACKED DIAGNOSTICS v_error_msg = MESSAGE_TEXT;
                    RAISE EXCEPTION '查找路径名称错误：%？%', LC_JD_MC, v_error_msg;
                END;
        END;
        IF LN_POS = 0 THEN
            RETURN LC_FJD_DM;
        END IF;
    END LOOP;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION P_GET_JD_NEW(
    AC_FJD_DM VARCHAR,
    AC_JD_MC  VARCHAR,
    AC_FLAG   VARCHAR
)
RETURNS VARCHAR(30)
AS $$
DECLARE
    LN_ROW    INTEGER;
    LN_COUNT  INTEGER;
BEGIN
    IF AC_FLAG = '0' THEN
        SELECT COUNT(*) INTO LN_ROW FROM QX_GNMK_TREE WHERE JD_DM = AC_FJD_DM AND (JDLX_DM = '01' OR JDLX_DM = '0');
        IF LN_ROW <> 1 THEN
            RAISE EXCEPTION '父节点代码：% 不是目录，无法向下扩展', AC_FJD_DM;
        END IF;

        SELECT COUNT(*) INTO LN_ROW FROM QX_GNMK_TREE WHERE FJD_DM = AC_FJD_DM AND JD_MC = AC_JD_MC;
        IF LN_ROW > 0 THEN
            RAISE EXCEPTION '节点名称重复：%', AC_JD_MC;
        END IF;

        IF LENGTH(AC_FJD_DM) > 18 THEN
            SELECT MAX(JD_DM) + 1 INTO LN_ROW FROM QX_GNMK_TREE WHERE JD_DM >= '0' AND JD_DM <= '9';
            RETURN LN_ROW::VARCHAR(30);
        ELSE
            SELECT MAX(JD_ORDER) INTO LN_ROW FROM QX_GNMK_TREE WHERE FJD_DM = AC_FJD_DM;
            IF LN_ROW IS NULL THEN
                LN_ROW := 1;
            ELSIF LN_ROW >= 999 THEN
                RAISE EXCEPTION '下属节点超过999，无法扩展！';
            ELSE
                LN_ROW := LN_ROW + 1;
            END IF;

            SELECT COUNT(*) INTO LN_COUNT FROM QX_GNMK_TREE WHERE JD_DM = AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
            WHILE LN_COUNT > 0 LOOP
                LN_ROW := LN_ROW + 1;
                SELECT COUNT(*) INTO LN_COUNT FROM QX_GNMK_TREE WHERE JD_DM = AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
            END LOOP;

            RETURN AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
        END IF;

    ELSE
        SELECT COUNT(*) INTO LN_ROW FROM QX_GNMB_GNMK WHERE JD_DM = AC_FJD_DM AND GNMK_DM = 'FFFFFFFFFFF' AND GNMB_DM = '00000000001';
        IF LN_ROW <> 1 THEN
            RAISE EXCEPTION '父节点代码：% 不是目录，无法向下扩展', AC_FJD_DM;
        END IF;

        SELECT COUNT(*) INTO LN_ROW FROM QX_GNMB_GNMK WHERE FJD_DM = AC_FJD_DM AND JD_MC = AC_JD_MC AND GNMB_DM = '00000000001';
        IF LN_ROW > 0 THEN
            RAISE EXCEPTION '节点名称重复：%', AC_JD_MC;
        END IF;

        IF LENGTH(AC_FJD_DM) > 18 THEN
            SELECT MAX(JD_DM) + 1 INTO LN_ROW FROM QX_GNMB_GNMK WHERE GNMB_DM = '00000000001' AND JD_DM >= '0' AND JD_DM <= '9';
            RETURN LN_ROW::VARCHAR(30);
        ELSE
            SELECT MAX(JD_ORDER) INTO LN_ROW FROM QX_GNMB_GNMK WHERE FJD_DM = AC_FJD_DM AND GNMB_DM = '00000000001';
            IF LN_ROW IS NULL THEN
                LN_ROW := 1;
            ELSIF LN_ROW >= 999 THEN
                RAISE EXCEPTION '下属节点超过999，无法扩展！';
            ELSE
                LN_ROW := LN_ROW + 1;
            END IF;

            SELECT COUNT(*) INTO LN_COUNT FROM QX_GNMB_GNMK WHERE JD_DM = AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
            WHILE LN_COUNT > 0 LOOP
                LN_ROW := LN_ROW + 1;
                SELECT COUNT(*) INTO LN_COUNT FROM QX_GNMB_GNMK WHERE JD_DM = AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
            END LOOP;

            RETURN AC_FJD_DM || LPAD(LN_ROW::VARCHAR(30), 3, '0');
        END IF;
    END IF;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE FUNCTION P_SEQUENCE_STANDARD(
    IN sequenceName VARCHAR,
    OUT sequenceNo VARCHAR(100),
    OUT ln_return  INTEGER
)
AS $$
DECLARE
    jdno    VARCHAR(4);
BEGIN
    EXECUTE 'select to_char(' || sequenceName || '.nextval)' INTO sequenceNo;

    SELECT P_GET_JDH() INTO jdno, ln_return;
    IF ln_return = 100 THEN
        sequenceNo := LPAD(sequenceNo, 8, '0');
        sequenceNo := jdno || TO_CHAR(CURRENT_TIMESTAMP, 'yy') || '9' || sequenceNo || '000';
    END IF;
END;
$$ LANGUAGE plpgsql;


