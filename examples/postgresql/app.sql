        SELECT MAX(JD_ORDER) INTO LN_ROW FROM QX_GNMB_GNMK WHERE FJD_DM = LC_FJD_DM AND GNMB_DM = '00000000001';
        IF LN_ROW IS NULL THEN
            LN_ROW := 1;
        ELSIF LN_ROW >= 999 THEN
